"""EXP 12 / E-C (GPU) — The portability matrix: is a summary's quality a
property of the SUMMARY or of the summarizer-executor PAIR?

Decomposes P2 (CompactionRL's transfer failure) without needing their
checkpoints: summaries generated by model S are consumed by executor E for
all (S, E) pairs. Both models share one tokenizer (verified), so contexts
transfer exactly.

  D[S][E] roughly constant across E  -> summary quality is representational
                                        (portable).
  D[S][S] << D[S][other E]           -> co-adaptation: models write summaries
                                        best consumed by themselves \u2014 the
                                        inference-level seed of the loop-
                                        adaptation effect.

    python exp12_portability.py --examples-file ../data/examples_64.json
"""

from __future__ import annotations

import argparse

from common import load_model, save_result, REPO  # noqa: F401
from behavior import sample_actions
from compressors import SUMMARIZE, _generate, _ids
from data import load_examples_file
from metrics import acting_rate, action_change

WRAP = ("<turn index=0 role=user>\n<content>\n[Summary of work so far: "
        "{s}]\n</content>\n</turn>\n")


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--models", nargs="+",
                    default=["Qwen/Qwen3.5-9B", "Qwen/Qwen3.5-4B"])
    ap.add_argument("--examples-file", required=True)
    ap.add_argument("--num-examples", type=int, default=16)
    ap.add_argument("--samples", type=int, default=8)
    ap.add_argument("--budget", type=int, default=256)
    ap.add_argument("--seed", type=int, default=0)
    args = ap.parse_args()

    loaded = {}
    for name in args.models:
        tok, model, device = load_model(name)
        loaded[name] = (tok, model, device)
    tok0 = loaded[args.models[0]][0]
    for name, (tok, _, _) in loaded.items():   # tokenizer identity guard
        s = "def f(x):\n    return x  # <tool_calls> test 123"
        assert tok(s)["input_ids"] == tok0(s)["input_ids"], \
            f"tokenizer mismatch for {name} - contexts would not transfer"

    examples = load_examples_file(args.examples_file, args.num_examples)
    D = {s: {e: [] for e in args.models} for s in args.models}
    ACT = {s: {e: [] for e in args.models} for s in args.models}
    floors = {e: [] for e in args.models}
    usable = 0

    for ei, ex in enumerate(examples):
        base = args.seed * 9973 + ei * 17
        old = ex.context_ids[: -len(ex.recent_ids)]

        refs = {}
        ok = True
        for e, (tok, model, device) in loaded.items():
            a = sample_actions(model, tok, ex.context_ids, device,
                               samples=args.samples, seed=base + 1)
            if acting_rate(a) < 0.5:
                ok = False
                break
            b = sample_actions(model, tok, ex.context_ids, device,
                               samples=args.samples, seed=base + 2)
            refs[e] = a
            floors[e].append(action_change(a, b))
        if not ok:
            print(f"example {ei}: skipped (an executor rarely acts)")
            continue
        usable += 1

        for s, (stok, smodel, sdev) in loaded.items():
            prompt = old + _ids(stok, SUMMARIZE)
            gen = _generate(smodel, stok, prompt, args.budget, sdev)
            text = stok.decode(gen, skip_special_tokens=True).strip()
            ctx = _ids(tok0, WRAP.format(s=text)) + ex.recent_ids
            for e, (etok, emodel, edev) in loaded.items():
                acts = sample_actions(emodel, etok, ctx, edev,
                                      samples=args.samples, seed=base + 3)
                D[s][e].append(action_change(refs[e], acts))
                ACT[s][e].append(acting_rate(acts))
        print(f"example {ei}: done")

    if not usable:
        print("no usable examples")
        return

    def avg(xs):
        return sum(xs) / len(xs) if xs else float("nan")

    short = {m: m.split("/")[-1] for m in args.models}
    print(f"\nusable: {usable}   floors: "
          + "  ".join(f"{short[e]}={avg(f):.2f}" for e, f in floors.items()))
    print("\nD[summarizer][executor] (acting):")
    label = "summarizer/executor"
    header = f"{label:>22s}" + "".join(f"{short[e]:>18s}" for e in args.models)
    print(header)
    rows = {}
    for s in args.models:
        cells = "".join(f"{avg(D[s][e]):8.2f} ({avg(ACT[s][e]):.2f}) "
                        for e in args.models)
        print(f"{short[s]:>22s}" + cells)
        rows[short[s]] = {short[e]: {"d": avg(D[s][e]), "acting": avg(ACT[s][e])}
                          for e in args.models}

    save_result("exp12_portability", {
        "models": args.models, "usable": usable,
        "floors": {short[e]: avg(f) for e, f in floors.items()},
        "matrix": rows,
        "raw": {f"{short[s]}->{short[e]}": D[s][e]
                for s in args.models for e in args.models},
    })


if __name__ == "__main__":
    main()
